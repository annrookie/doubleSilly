package com.rookie.common.util;

/**
 * @author hanhan
 */
public class PrintUtil {

    /**
     * 换行打印
     *
     * @param obj 对象
     */
    public static void print(Object obj) {
        System.out.println(obj);
    }

    /**
     * 不换行打印
     *
     * @param obj 对象
     */
    public static void printb(Object obj) {
        System.out.print(obj);
    }

}
